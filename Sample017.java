public class Sample017 {
    public static void main(String[] args) {
        // 変数を宣言し初期値を設定する
        int a = 20;
        int b = 4;

        // 演算を行う
        System.out.println("a + b は" + (a + b) + " です");
        System.out.println("a - b は" + (a - b) + " です");
        System.out.println("a * b は" + (a * b) + " です");
        System.out.println("a / b は" + (a / b) + " です");
        System.out.println("a % b は" + (a % b) + " です");
    }
}
