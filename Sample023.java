public class Sample023 {
    public static void main(String[] args) {
        int a = 20;

        switch (a) {
            case 10:
                System.out.println("aは10です");
                break;
            case 20:
                System.out.println("aは20です");
                break;
            default:
                System.out.println("aは10でも20でもありません");
                break;
        }
    }
}
